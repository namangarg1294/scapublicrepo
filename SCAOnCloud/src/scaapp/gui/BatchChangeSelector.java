public void changeBatchSelector(){
    batches.removeAllItems();
    int id=1;
    if(man.isSelected())
        id=1;
    else
        id=2;
      ArrayList <String> batchesList=null;
        try {
            batchesList=BatchDao.getAllActiveBatchId(id);
        } catch (SQLException ex) {
            Logger.getLogger(FeePayment.class.getName()).log(Level.SEVERE, null, ex);
        }
         for(String s: batchesList)
          batches.addItem(s);
    
}


